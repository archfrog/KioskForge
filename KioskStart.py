# This script is used to start the kiosk by configuring sound (if applicable), loading X11 or the user's app, etc.

# Import Python v3.x's type hints as these are used extensively in order to allow MyPy to perform static checks on the code.
from typing import List

import os
import platform
import sys
import time

from toolbox.builder import TextBuilder
from toolbox.driver import KioskDriver
from toolbox.errors import CommandError, KioskError
from toolbox.internet import lan_ip_address
from toolbox.invoke import invoke_list_safe, invoke_text_safe
from toolbox.logger import Logger
from toolbox.setup import Setup
from toolbox.version import Version

CARDS = {
	'pi4b.jack'  : 'alsa_output.platform-bcm2835_audio.stereo-fallback',
	'pi4b.hdmi1' : 'alsa_output.platform-fef00700.hdmi.hdmi-stereo',
	'pi4b.hdmi2' : 'alsa_output.platform-fef05700.hdmi.hdmi-stereo',
	'pi5.hdmi1'  : 'alsa_card.platform-107c701400.hdmi.hdmi-stereo',
	'pi5.hdmi2'  : 'alsa_card.platform-107c706400.hdmi.hdmi-stereo'
}

class KioskStart(KioskDriver):
	"""Defines the KioskStart.py script, which is responsible for starting up the kiosk after it has been forged."""

	def __init__(self) -> None:
		KioskDriver.__init__(self)
		self.version = Version(self.project)

	def _main(self, logger : Logger, origin : str, arguments : List[str]) -> None:
		# Check that we're running on Linux.
		if platform.system() != "Linux":
			raise KioskError("This script is can only be run on a Linux kiosk machine")

		# Parse command-line arguments.
		if len(arguments) != 0:						# pylint: disable=duplicate-code
			raise CommandError('"KioskStart.py"')

		# Display LAN IP - not everybody has access to the router in charge of assigning a LAN IP via DHCP.
		logger.write(lan_ip_address())

		# Load settings generated by KioskForge on the desktop machine.
		setup = Setup()
		setup.load(origin + os.sep + "KioskForge.kiosk")

		# Configure audio subsystem, if applicable.
		if setup.sound_card.data != "none":
			# TODO: Incorporate https://www.reddit.com/r/pipewire/comments/13w2xyk/comment/jmj138k/ (use 'wpctl' even if it is sad).

			# NOTE: 'wpctl' only accepts ids so we cheat and ask PulseAudio's pactl to do the job for us.
			# Pause for three seconds because pactl otherwise reports spurious errors (the audio subsystem is not up yet, I guess).
			time.sleep(3)

			# Set default output device to that of the 'device' option.
			card_path = setup.device.data + '.' + setup.sound_card.data
			# TODO: Fail gracefully if the card path isn't defined (PI5, for instance).
			card_name = CARDS[card_path]
			invoke_text_safe(f"pactl set-default-sink {card_name}")
			del card_name
			del card_path

			# Set the audio level to user-specified percentage on a logarithmic scale.
			invoke_text_safe(f"wpctl set-volume @DEFAULT_AUDIO_SINK@ {setup.sound_level.data / 100.0:.2f}")

		if setup.type.data in [ "x11", "web" ]:
			# Only execute the request if $DISPLAY is undefined and $XDG_VTNR is equal to 1 (avoid starting X11 twice).
			if not os.environ.get("DISPLAY") and os.environ.get("XDG_VTNR") == "1":
				# Launch X11, which runs '.config/openbox/autostart' to eventually launch Chromium or user app in kiosk mode.
				words  = TextBuilder()
				words += "startx"
				if not setup.mouse.data:
					words += "--"
					words += "-nocursor"
				invoke_list_safe(words.list)
		elif setup.type.data in ["cli"]:
			invoke_text_safe(setup.command.data)
		else:
			raise KioskError(f"Unknown kiosk type: {setup.type.data}")


if __name__ == "__main__":
	sys.exit(KioskStart().main(sys.argv))

